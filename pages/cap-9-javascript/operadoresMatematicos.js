let primeiroNumero = 50;
let segundoNumero = 10;

console.log(`Primeiro numero: ${primeiroNumero}, Tipo: ${typeof primeiroNumero}`);
console.log(`Segundo numero: ${segundoNumero}, Tipo: ${typeof segundoNumero}`);

// Adição

console.log(`Soma: ${primeiroNumero + segundoNumero}`);
// ou
soma = primeiroNumero + segundoNumero;
console.log("soma: " + soma);
// mais estetico
console.log(`${primeiroNumero} + ${segundoNumero} = ${soma}`);

// Subtração

console.log(`Subtração: ${primeiroNumero - segundoNumero}`);

// Multiplicação   
console.log(`Multiplicação: ${primeiroNumero * segundoNumero}`);

// Divisão
console.log(`Divisão: ${primeiroNumero / segundoNumero}`);      

// Porcentagem
console.log(`Porcentagem: ${primeiroNumero * segundoNumero / 100}`);




